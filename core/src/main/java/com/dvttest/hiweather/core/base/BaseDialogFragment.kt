package com.dvttest.hiweather.core.base

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.annotation.LayoutRes
import androidx.databinding.DataBindingUtil
import androidx.databinding.ViewDataBinding
import com.dvttest.hiweather.core.R
import com.google.android.material.bottomsheet.BottomSheetDialogFragment

abstract class BaseDialogFragment<B : ViewDataBinding>(@LayoutRes private val layoutId: Int) : BottomSheetDialogFragment() {

    lateinit var viewBinding: B

    override fun getTheme(): Int = R.style.ThemeOverlay_HiWeatherTheme_BottomSheetDialog

    override fun onCreateView(inflater: LayoutInflater, container: ViewGroup?, savedInstanceState: Bundle?): View? {
        viewBinding = DataBindingUtil.inflate(inflater, layoutId, container, false)
        viewBinding.lifecycleOwner = viewLifecycleOwner
        return viewBinding.root
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        onInitDataBinding()
    }

    /**
     * Called to Initialize view data binding variables when fragment view is created.
     */
    abstract fun onInitDataBinding()
}
